const mongoose = require('mongoose');

const Schema = mongoose.Schema;

const productSchema = new Schema({ //This is what a product should look like in the application
  //key value pairs
  title: {
    type: String,
    required: true //This gives up flexibility but helps prevent errors 
  },
  price: {
    type: Number,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  imageUrl: {
    type: String,
    required: true
  },
  userId: {
    type: Schema.Types.ObjectId,
    ref: 'User', //Use the name of the model 
    required: true
  }
});


module.exports = mongoose.model('Product', productSchema); 
